int		ft_is_prime(int nb)
{
	int i;
	int count;

	i = 2;
	count = 0;
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= nb)
	{
		if (nb % i == 0)
			count++;
		i++;
	}
	if (count == 1)
		return (1);
	return (0);
}
